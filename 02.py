# 2.簡単な計算ゲーム
print("\nまずは計算してみよう!")
num1 = int(input("1つ目の数字を入れてね:"))
num2 = int(input("2つ目の数字を入れてね:"))

print(num1, "+", num2, "=", num1 + num2)
print(num1, "×", num2, "=", num1 * num2) 

#3 簡単な計算ゲーム
if num1 * num2 > 100:
    print("おお!掛け声の結果が100を超えた!すごいね!")
elif num1 * num2 > 50:
    print("なかなかいい感じの数字だね～")
else:
    print("もう少し大きい数字で遊んでみようか?")

#4 forループで繰り返し(カウントダウン風)
print("\n次はおみくじの準備…3秒カウントダウン!")
